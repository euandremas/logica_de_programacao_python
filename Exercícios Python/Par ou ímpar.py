numero =int(input("Digite um número e lhe direi se ele é par ou ímpra: "))  
if numero % 2 == 0:
    print("O número {} é par".format(numero))
else:
    print("O número {} é ímpar".format(numero))
# O operador % retorna o resto da divisão entre dois números.
# Se o resto da divisão do número por 2 for igual a 0, o número é par.
# Caso contrário, o número é ímpar.
# O operador % é chamado de operador de módulo e é usado para encontrar o resto da divisão entre dois números.
# O operador % é muito útil em várias situações, como verificar se um número é par ou ímpar, ou encontrar múltiplos de um número.
# O operador % é um operador binário, o que significa que ele opera em dois operandos.
# O operador % é um operador de comparação, o que significa que ele compara dois valores e retorna um valor booleano (True ou False).
# O operador % é um operador aritmético, o que significa que ele realiza uma operação matemática em dois números.